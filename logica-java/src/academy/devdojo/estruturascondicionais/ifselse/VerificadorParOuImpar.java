public class VerificadorParOuImpar {

    public static void main(String[] args){

        int numero = 84;
        int resultado = numero % 2;

        if(resultado == 0){
            System.out.println("O número é par");

        }else{
            System.out.println("O número é ímpar");

        }


    }


}
